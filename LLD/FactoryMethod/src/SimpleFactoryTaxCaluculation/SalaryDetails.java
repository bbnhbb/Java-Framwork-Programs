package SimpleFactoryTaxCaluculation;

public class SalaryDetails {

    public Float hra;
    public Float lta;
    public Float basePay;

    public void setHra(Float hra) {
        this.hra = hra;
    }

    public void setLta(Float lta) {
        this.lta = lta;
    }

    public void setBasePay(Float basePay) {
        this.basePay = basePay;
    }

    public Float getHra() {
        return hra;
    }

    public Float getLta() {
        return lta;
    }

    public Float getBasePay() {
        return basePay;
    }
}
